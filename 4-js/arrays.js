// Creating an array with a specified length
const names = ["Maaxboon", "Uri-X", "Poisedon", "Jok-Maker"];

// Accessing the elements of an array
console.log(names[0]); // Maaxboon
console.log(names[1]); // Uri-X
console.log(names[2]); // Poisedon
console.log(names[3]); // Jok-Maker

// Creating an empty array
const colors = [];
console.log(colors);

// Adding values
colors.push("red");
console.log(colors);
colors.push("blue");
console.log(colors);

// Adding to the beginning of an array
colors.unshift("green");
console.log(colors);

// Adding values via index notation
colors[3] = "orange";
colors[4] = "yellow";
console.log(colors);
